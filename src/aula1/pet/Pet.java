package aula1.pet;

public class Pet {
    String type;
    String name;
    int age;
    float amtFood;
    String sound;

    public Pet(String type, String name, int age, float amtFood, String sound) {
        this.type = type;
        this.name = name;
        this.age = age;
        this.amtFood = amtFood;
        this.sound = sound;
    }
}
